<script>
import "../assets/tailwind.css";
import Connexion from "../views/restaurateurs/Connexion.vue";
const token = localStorage.getItem("token");

export default {
  name: "navbar",
  data() {
    return {
      token: token,
      lastname: "",
      firstname: "",
      show: false,
    };
  },

  async mounted() {
    const options = {
      method: "GET",
      headers: {
        "Content-Type": "application/json",
        Accept: "application/json",
        Authorization: "Bearer " + localStorage.getItem("token"),
      },
    };

    const response = await fetch(
      "https://checkeat-api.osc-fr1.scalingo.io/api/restaurateurs",
      options
    );

    const data = await response.json();

    const profil = data.restaurateur;

    this.lastname = profil.lastname;
    this.firstname = profil.firstname;
  },

  methods: {
    async logout() {
      const options = {
        method: "GET",
        headers: {
          "Content-Type": "application/json",
          Accept: "application/json",
          Authorization: "Bearer " + localStorage.getItem("token"),
        },
      };
      const response = await fetch(
        "https://checkeat-api.osc-fr1.scalingo.io/api/logout",
        options
      );

      const data = await response.json();

      localStorage.removeItem("token", data.access_token);
      // location = "http://localhost:8080/";
      // this.$router.replace({ path: "/" });
      document.location.href = "/";
    },
  },
};
</script>

